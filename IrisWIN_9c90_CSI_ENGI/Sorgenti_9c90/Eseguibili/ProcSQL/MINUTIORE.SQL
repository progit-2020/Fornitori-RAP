CREATE OR REPLACE FUNCTION MINUTIORE (MINUTI IN INTEGER) RETURN VARCHAR2 AS
  HH VARCHAR2(20);
BEGIN
  HH:=NULL;
  IF MINUTI IS NOT NULL THEN
    HH:=TRUNC(ABS(MINUTI)/60) || '.' || LPAD(MOD(ABS(MINUTI),60),2,'0');
    IF MINUTI < 0 THEN
      HH:='-' || HH;
    END IF;
  END IF;
  RETURN HH; 
END;
/
