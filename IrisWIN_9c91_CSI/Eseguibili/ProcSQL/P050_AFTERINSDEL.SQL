CREATE OR REPLACE TRIGGER P050_AFTERINSDEL
AFTER INSERT OR DELETE
ON P050_ARROTONDAMENTI
DECLARE
  P NUMBER;
BEGIN
  IF INSERTING THEN
    INSERT INTO P050_KARROTONDAMENTI
    (COD_ARROTONDAMENTO)
      SELECT COD_ARROTONDAMENTO FROM P050_ARROTONDAMENTI
      MINUS
      SELECT COD_ARROTONDAMENTO FROM P050_KARROTONDAMENTI;
  ELSIF DELETING THEN
    DELETE FROM P050_KARROTONDAMENTI WHERE
    COD_ARROTONDAMENTO IN
      (SELECT COD_ARROTONDAMENTO FROM P050_KARROTONDAMENTI
      MINUS
      SELECT COD_ARROTONDAMENTO FROM P050_ARROTONDAMENTI);
  END IF;
END;
/
